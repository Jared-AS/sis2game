    public String[] getListaPiezas() {
        String[] indices = new String[16];
        int cont = 0;
        for (int i = 0; i < 4; i++) {
            for (int j = 0; j < 4; j++) {
                indices[cont] = "" + tablero.get(i, j);
                cont++;
            }
        }
        return indices;
    }

