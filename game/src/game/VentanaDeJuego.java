    public boolean WIN() {
        int cont = 1;
        int aux = 0;
        boolean win = false;
        for (int i = 0; i < 4; i++) {
            for (int j = 0; j < 4; j++) {
                if (cont == tablero.get(i, j)) {
                    aux++;
                }
                cont++;
            }
        }
        if (aux == 16) {
            win = true;
        }
        return win;
    }


